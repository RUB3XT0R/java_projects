    private void btnloginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnloginMouseClicked
        if( ! Ferramentas.isCompleteFields(txtlogin,txtsenha) ) return;
        try {
            l = dao.isValidUserAndPassword(txtlogin.getText(),txtsenha.getText());
            if(l==null){
                Ferramentas.showErro("Login ou Senha inv√°lidos!","Erro!");
                return;
            }
            Menu cm = new Menu();
            cm.getUser(l,this);
            cm.setVisible(true);
            Ferramentas.limparCampos(txtlogin,txtsenha);
            this.setVisible(false);
        } catch (SQLException ex) {
            Ferramentas.showErro("Erro ao efetuar o login.","Erro!");
            System.out.println(ex);
        }
    }//GEN-LAST:event_btnloginMouseClicked

