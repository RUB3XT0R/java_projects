    private boolean validate(JTextField tx){
        if ("".equals(tx.getText()) | Float.parseFloat(tx.getText())==0f) {
            JOptionPane.showMessageDialog(null,"Entre com um ângulo válido.","Erro!",JOptionPane.ERROR_MESSAGE);
            tx.requestFocus();
            return false;
        }
        return true;
    }
    
