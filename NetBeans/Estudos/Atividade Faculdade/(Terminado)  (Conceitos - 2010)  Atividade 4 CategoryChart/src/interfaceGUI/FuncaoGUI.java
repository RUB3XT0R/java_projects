    private void jLabel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel1MouseClicked
        try{
            String str= new String();
            FuncaoCalc fc = new FuncaoCalc();
            //capturar as informações
            fim = Float.parseFloat(jTextField5.getText());
            inicio=Float.parseFloat(jTextField4.getText());
            complemento=Float.parseFloat(jTextField6.getText());
            a=Double.parseDouble(jTextField1.getText());
            b=Double.parseDouble(jTextField2.getText());
            c=Double.parseDouble(jTextField3.getText());

            if(inicio>fim){
                JOptionPane.showMessageDialog(null,
                        "O valor do 'inicio dos dados' deve ser menor que o valor do 'fim dos dados'.",
                        "Aviso!",JOptionPane.ERROR_MESSAGE);
                return;
            }
            if(complemento<0){
                JOptionPane.showMessageDialog(null,
                        "O valor do complemento(escala) não deve ser menor ou igual a zero."
                        +"\nEntre apenas com um número positivo.","Aviso!",JOptionPane.ERROR_MESSAGE);
                return;
            }
            //dimensões da barra de progresso
            jProgressBar1.setMaximum((int) fim);
            jProgressBar1.setMinimum((int) inicio);
            //preenchimento da lista de resultados e da barra de progreso
            for(float i=inicio;i <= fim; i=i+complemento) {
                jProgressBar1.setValue(jProgressBar1.getValue()+ (int)complemento);
                paintComponents(this.getGraphics());
                DecimalFormat df =new DecimalFormat("#,###.00");
                str="F("+i+")="+df.format(fc.resultCalc(a, b, c, i));
                listaModel.addElement( new String(str));
                //preenchimento do componente jList1
                jList1.setModel( listaModel );
                //preenchimento da base de dados para o gráfico
                First.dataset.addValue(fc.resultCalc(a, b, c, i),"Entrada",i+"");
            
            }
            //definindo o titulo do gráfico
            First.funcao="("+a+"x²) + ("+b+"x) + ("+c+")";

            jProgressBar1.setValue((int) fim);
            jLabel11.setForeground(Color.WHITE);
            jLabel10.setVisible(true);

        }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(null,"Você esqueceu de digitar uma das informações ou digitou o tipo errado de dado.\nDigite apenas numeros.","Aviso!",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jLabel1MouseClicked
    
