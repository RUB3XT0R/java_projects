    public VenderGUI(Consulta cons) {
        initComponents();
        this.cons=cons;

        // Carga do ComboBox Lanche
        i = (cons.carregaLanche()).elements();
        while(i.hasMoreElements()){
            jComboBox1.addItem(i.nextElement());
        }

        // Carga da data
        hoje = new Date();
        jTextField2.setText(dt.format(hoje));

        // Travamento do número da transação no banco
        jLabel2.setText(cons.incluirVenda());
    }

    public boolean consiste(){
        try{
            // O formato do preço tem que ser válido
            quantidade = Integer.parseInt(jTextField1.getText().toString());
            // O preço nãopode ser zero
            if (quantidade==0) {
                jLabel3.setText("A quantidade não pode ser zero.");
                jTextField1.setText(null);
                jTextField1.requestFocus();
                return false;
            }

            if(dlm.toString().contains(jComboBox1.getSelectedItem().toString())){
                jLabel3.setText("Lanche repetido");
                jTextField1.setText(null);
                jTextField1.requestFocus();
                return false;
            }
        }catch(NumberFormatException e){
             jLabel3.setText("Formato numérico inválido");
             jTextField1.setText(null);
             jTextField1.requestFocus();
             return false;
        }
        return true;
    }

