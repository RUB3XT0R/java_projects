    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if(!dlm.isEmpty()){
             if(javax.swing.JOptionPane.showConfirmDialog(null,
                    "Confirmação de gravação",
                    "Confirmação",
                    javax.swing.JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
                 //gravar
                 cons.gravaVenda(dlm,hoje,jLabel2.getText().toString(),quantidade);
             }else{
                 cons.apagaVenda(jLabel2.getText().toString());
             }
        }else{
            cons.apagaVenda(jLabel2.getText().toString());
        }
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

